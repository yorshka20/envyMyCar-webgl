// a function for inferring the type of an object.
// generate by chatGPT 3.5
function inferObjectType(obj: any) {
  const inferredType = {} as any;

  for (const key in obj) {
    if (typeof obj[key] === 'object' && !Array.isArray(obj[key])) {
      inferredType[key] = inferObjectType(obj[key]);
    } else {
      inferredType[key] = typeof obj[key];
    }
  }

  return inferredType;
}

